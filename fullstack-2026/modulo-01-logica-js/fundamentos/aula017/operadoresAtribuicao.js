/**
 * Operadores de Atribuição Combinada: Estes operadores realizam
 * uma operação aritmética e atribuem o resultado à variável.
 */
let a = 5;
a += 3;  // Equivalente a: a = a + 3; (adiciona 3 a 'a')

let b = 8;
b -= 2;  // Equivalente a: b = b - 2; (subtrai 2 de 'b')

let c = 4;
c *= 6;  // Equivalente a: c = c * 6; (multiplica 'c' por 6)

let d = 9;
d /= 3;  // Equivalente a: d = d / 3; (divide 'd' por 3)

let e = 10;
e %= 3;  // Equivalente a: e = e % 3; (atribui o resto da divisão de 'e' por 3 a 'e')