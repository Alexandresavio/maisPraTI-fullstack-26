/**
 * Em JavaScript, os operadores aritméticos são utilizados 
 * para realizar operações matemáticas em valores numéricos.
 */

//Adição (+): Adiciona dois valores:
let adicao = 5 + 3; // resultado é 8
console.log(adicao);

//Subtração (-): Subtrai o valor à direita do valor à esquerda;
let subtracao = 5 - 3; // resultado é 8
console.log(subtracao);

//Multiplicação (*): Multiplica dois valores;
let multi = 5 * 3; // resultado é 15
console.log(multi);

//Divisão (/): Divide o valor à esquerda pelo valor à direita;
let divisao = 15 / 3;
console.log(divisao);

//Módulo (%): Retorna o resto da divisão;
let modulo = 15 % 3;
console.log(modulo);